    private void bSelesaiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSelesaiActionPerformed
        // TODO add your handling code here:
        String nama = eNama.getText();
        String kelas = eKelas.getText();
        String sekolah = eSekolah.getText();
        String gambar = eGambar.getText();

        String message = "Nama: " + nama + "\nKelas: " + kelas + "\nSekolah: " + sekolah;

        // Create the ImageIcon for the image
        ImageIcon imageIcon = new ImageIcon(gambar); // Load the image
        java.awt.Image img = imageIcon.getImage(); // Transform it
        java.awt.Image newImg = img.getScaledInstance(100, 100, java.awt.Image.SCALE_SMOOTH); // Scale it for the JOptionPane
        imageIcon = new ImageIcon(newImg);  // Convert it back to an ImageIcon

        // Display the message with the image in a JOptionPane
        JOptionPane.showMessageDialog(null, message, "Informasi Siswa", JOptionPane.INFORMATION_MESSAGE, imageIcon);
    }//GEN-LAST:event_bSelesaiActionPerformed

