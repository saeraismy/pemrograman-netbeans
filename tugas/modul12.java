        JFileChooser pilih = new JFileChooser();
        pilih.showOpenDialog(null);
        File f = pilih.getSelectedFile();
        String namafile = f.getAbsolutePath();
        eGambar.setText(namafile);

        ImageIcon imageIcon = new ImageIcon(namafile); // Load the image
        java.awt.Image img = imageIcon.getImage(); // Transform it
